function showResults(imgElement, classes) {
  let cat = classes.filter(d => {
    return [281, 282, 283, 284, 285].includes(d.classIndex)
  }).length > 0;
  let result = cat ? "YES" : "NO";
  console.log(imgElement)

  predictionElement.innerText =  result;
  let catElement = document.getElementById('cat');
  catElement.replaceWith(imgElement);
}

const filesElement = document.getElementById('files');
filesElement.addEventListener('change', evt => {
  let files = evt.target.files;
  // Display thumbnails & issue call to predict each image.
  for (let i = 0, f; f = files[i]; i++) {
    // Only process image files (skip non image files)
    if (!f.type.match('image.*')) {
      continue;
    }
    let reader = new FileReader();
    reader.onload = e => {
      // Fill the image & call predict.
      let img = document.createElement('img');
      img.id = 'cat';
      img.className = 'mx-auto';
      img.src = e.target.result;
      img.width = IMAGE_SIZE;
      img.height = IMAGE_SIZE;
      img.onload = () => predict(img);
    };

    // Read in the image file as a data URL.
    reader.readAsDataURL(f);
  }
});

